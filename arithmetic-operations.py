n1 = int(input("Enter number 1: "))
n2 = int(input("Enter number 2: "))

add = n1 + n2
print('Addition of ', n1, ' & ', n2, ' is ', add)

sub = n1 - n2
print('Subtraction of ', n1, ' & ', n2, ' is ', sub)

mul = n1 * n2
print('Multiplication of ', n1, ' & ', n2, ' is ', mul)

divFloat = n1 / n2
print('Float Division of ', n1, ' & ', n2, ' is ', divFloat)

divInt = n1 // n2
print('Integer Division of ', n1, ' & ', n2, ' is ', divInt)

power = n1 ** n2
print(n1, ' to the power ', n2, ' is ', power)
